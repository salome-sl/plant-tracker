// Walk text nodes + a few attributes and swap in Dutch. Idempotent: Dutch text
// won't match the English keys, so re-running is safe. No-op in English.
export function localizeDOM(root) {
  if (getLang() === 'en' || !root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const p = node.parentElement;
      if (!p) return NodeFilter.FILTER_REJECT;
      const tag = p.tagName;
      if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'TEXTAREA') return NodeFilter.FILTER_REJECT;
      if (p.closest('[data-noloc]')) return NodeFilter.FILTER_REJECT;
      return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    },
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const n of nodes) {
    const out = tr(n.nodeValue);
    if (out != null && out !== n.nodeValue) n.nodeValue = out;
  }
  root.querySelectorAll('[placeholder]').forEach((e) => {
    const out = tr(e.getAttribute('placeholder'));
    if (out != null) e.setAttribute('placeholder', out);
  });
  root.querySelectorAll('[title]').forEach((e) => {
    const out = tr(e.getAttribute('title'));
    if (out != null) e.setAttribute('title', out);
  });
}

