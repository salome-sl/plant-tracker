// i18n.js — Language (English/Dutch) + units (metric/imperial).
//
// Strategy: content and UI stay authored in English in the code. When the user
// picks Dutch we translate the *rendered* DOM after each render (localizeDOM),
// using the dictionaries below. Generative text (the care coach, seasons, dates)
// is localized at its source instead, and a few interpolated strings use t().
//
// Units are independent of language and are converted at the source (heights are
// stored canonically in cm; temperatures come from the data in °C).

import { getSettings } from './settings.js';

export function getLang() { return getSettings().lang || 'en'; }
export function getUnits() { return getSettings().units || 'metric'; }

// ---- Units --------------------------------------------------------------
export function heightUnit() { return getUnits() === 'imperial' ? 'in' : 'cm'; }

// Display number (in the current unit) → canonical cm for storage.
export function heightToCm(value) {
  const n = parseFloat(value);
  if (!isFinite(n)) return null;
  return getUnits() === 'imperial' ? n * 2.54 : n;
}

// A stored cm value → a tidy string in the current unit, e.g. "24 cm" / "9.4 in".
export function fmtHeight(cm) {
  if (cm == null || !isFinite(cm)) return '';
  const v = getUnits() === 'imperial' ? cm / 2.54 : cm;
  const r = Math.round(v * 10) / 10;
  return `${Number.isInteger(r) ? r : r.toFixed(1)} ${heightUnit()}`;
}
// Just the number part (for chart captions that add the unit once).
export function heightNum(cm) {
  if (cm == null || !isFinite(cm)) return '';
  const v = getUnits() === 'imperial' ? cm / 2.54 : cm;
  const r = Math.round(v * 10) / 10;
  return Number.isInteger(r) ? String(r) : r.toFixed(1);
}

export function fmtTemp(celsius) {
  if (!isFinite(celsius)) return '';
  if (getUnits() === 'imperial') return `${Math.round(celsius * 9 / 5 + 32)}°F`;
  return `${celsius}°C`;
}

// ---- Interpolated strings (source-level) --------------------------------
// t('Due {rel}', { rel }) — used at the handful of composed call sites.
export function t(key, vars) {
  let s = (getLang() !== 'en' && TEMPLATES_NL[key]) || key;
  if (vars) for (const k in vars) s = s.split('{' + k + '}').join(vars[k]);
  return s;
}

const TEMPLATES_NL = {
  'Due {rel}': 'Over {rel}',
  'Water {rel}': 'Water {rel}',
  '{n} days overdue': '{n} dagen te laat',
  'due {rel}': '{rel}',
};

// Translate a literal string (exact dictionary lookup); returns it unchanged in
// English or when there's no entry. Used by the care coach / seasons for tokens
// they splice into generated sentences (light level, humidity, season names).
export function tl(s) {
  return (getLang() !== 'en' && NL[s]) || s;
}

const SEASON_NL = { Spring: 'Lente', Summer: 'Zomer', Autumn: 'Herfst', Winter: 'Winter' };
const HEMI_NL = { Northern: 'Noordelijk', Southern: 'Zuidelijk' };

// ---- DOM localization pass ----------------------------------------------
// Exact-match dictionary first, then a few regex patterns for numeric/composed
// strings. Patterns can recurse via tr() so "every 5 days · last: …" localizes
// both halves. Returns the Dutch string, or null if nothing matched.
function tr(str) {
  // Try the raw string first so dictionary keys that carry intentional leading
  // or trailing spaces (e.g. "Signs: ", " Show key") match exactly.
  if (Object.prototype.hasOwnProperty.call(NL, str)) return NL[str];
  const key = str.trim();
  if (!key) return null;
  if (Object.prototype.hasOwnProperty.call(NL, key)) {
    return str.replace(key, NL[key]);
  }
  for (const [re, fn] of PATTERNS) {
    const m = key.match(re);
    if (m) return str.replace(key, fn(m));
  }
  return null;
}

